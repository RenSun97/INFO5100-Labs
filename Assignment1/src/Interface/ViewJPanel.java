    private void btnViewDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailActionPerformed
        int selectedrow = tblVitalSign.getSelectedRow();
        if(selectedrow >= 0)
        {
            CreatePerson cp =(CreatePerson)tblVitalSign.getValueAt(selectedrow, 0);
            txtName.setText(cp.getName());
            txtDataofbirth.setText(cp.getDateOfBirth());
            txtTelephoneNum.setText(cp.getTelephoneNumbers());
            txtGographicData.setText(cp.getGographicData());
            txtSocialSecurityNumber.setText(cp.getSocialSecurityNumber());
            txtFAXNumber.setText(cp.getFAXNumber());
            txtEmailAdd.setText(cp.getEmailAddresses());
            txtSocialSecurityNumber.setText(cp.getSocialSecurityNumber());
            txtMedicalRecordNumber.setText(cp.getMedicalRecordNumber());
            txtHealthPlanBeneficiaryNumber.setText(cp.getHealthPlanBeneficiaryNumber());
            txtBankAccountNumbers.setText(cp.getBankAccountNumbers());
            txtCertificatecenseNumber.setText(cp.getCertificatecenseNumber());
            txtVehicleIdentifiers.setText(cp.getVehicleIdentifiers());
            txtDeviceIdentifiers.setText(cp.getDeviceIdentifiers());
            txtLinkedIn.setText(cp.getLinkedIn());
            txtInternetProtocolAddresses.setText(cp.getInternetProtocolAddresses());
            txtBiometricIdentifiers.setText(cp.getBiometricIdentifiers());
            txtUniqueCode.setText(cp.getUniqueCode());
            Icon icon =  new ImageIcon(cp.getFilePath());
            lblImage.setIcon(icon);
        }
        else
            JOptionPane.showMessageDialog(null, "Please select other.");
            
    }//GEN-LAST:event_btnViewDetailActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedrow = tblVitalSign.getSelectedRow();
        if(selectedrow >= 0)
        {
            CreatePerson cp =(CreatePerson)tblVitalSign.getValueAt(selectedrow, 0);
            cph.deleteVitals(cp);
            JOptionPane.showMessageDialog(null, "Success");
            populateTable();
        }
        else
            JOptionPane.showMessageDialog(null, "Please select other.");
    }//GEN-LAST:event_btnDeleteActionPerformed

