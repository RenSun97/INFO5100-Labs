    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblVitalSign.getModel();
        dtm.setRowCount(0);
        
        for ( CreatePerson cp : cph.getCreatePHistory()){
            Object row[] = new Object[2];
            row[0] = cp;
            row[1] = cp.getSocialSecurityNumber();
            dtm.addRow(row);
        }
    }

