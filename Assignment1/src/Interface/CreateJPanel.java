        c.setBankAccountNumbers(BankAccountNumbers);
        c.setCertificatecenseNumber(CertificatecenseNumber);
        c.setVehicleIdentifiers(VehicleIdentifiers);
        c.setDeviceIdentifiers(DeviceIdentifiers);
        c.setLinkedIn(LinkedIn);
        c.setInternetProtocolAddresses(InternetProtocolAddresses);
        c.setBiometricIdentifiers(BiometricIdentifiers);
        c.setFilePath(filePath);
        c.setUniqueCode(UniqueCode);

