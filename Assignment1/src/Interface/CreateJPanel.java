        c.setDateOfBirth(DateofBirth);
        c.setGographicData(gogrphicDate);
        c.setTelephoneNumbers(TelephoneNumbers);
        c.setFAXNumber(FAXNumber);
        c.setEmailAddresses(EmailAddresses);
        c.setSocialSecurityNumber(SocialSecurityNumber);
        c.setMedicalRecordNumber(MedicalRecordNumber);
