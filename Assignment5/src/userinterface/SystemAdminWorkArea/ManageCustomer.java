    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabCustomers.getSelectedRow();
        if (selectedRow >= 0) {
            String username = (String) tabCustomers.getValueAt(selectedRow, 0).toString();//********************
            if(this.ecosystem.getUserAccountDirectory().removeUserAccount(username))
            {
                JOptionPane.showMessageDialog(null, "Customer deleted.");
                this.populate();
            }
                
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabCustomers.getSelectedRow();
        if (selectedRow >= 0) {
            String username1=tabCustomers.getValueAt(selectedRow, 0).toString();//**********************
            String username=this.txtUsername.getText();
            String password=this.txtPassword.getText();

            if(ecosystem.checkIfUserIsUnique(username))
            {
                JOptionPane.showMessageDialog(null, "Customer already exist!");

            }
            else
            {
                ecosystem.getUserAccountDirectory().searchUserAccount(username1).setUsername(username);
                ecosystem.getUserAccountDirectory().searchUserAccount(username).setPassword(password);
                JOptionPane.showMessageDialog(null, "Customer updated successfully!");
                 populate();
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        CardLayout layout=(CardLayout)this.userProcessContainer.getLayout();
        this.userProcessContainer.remove(this);
        layout.previous(userProcessContainer);
        Component[] comps=this.userProcessContainer.getComponents();
        for(Component comp:comps)
        {
            if(comp instanceof SystemAdminWorkAreaJPanel)
            {
                SystemAdminWorkAreaJPanel manage=(SystemAdminWorkAreaJPanel)comp;
                manage.populateTree();
            }
        }
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
         String username=this.txtUsername.getText();
        String password=this.txtPassword.getText();
        if(this.ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(username))
        {
            Role role=new CustomerRole();
            this.ecosystem.getUserAccountDirectory().createUserAccount(username, password, null, role);
            JOptionPane.showMessageDialog(null, "Customer created successfully!");
            populate();
        }
        else
            JOptionPane.showMessageDialog(null, "Customer already exist!");
        
    }//GEN-LAST:event_btnCreateActionPerformed

