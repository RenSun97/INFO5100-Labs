    public void populate()
    {
        DefaultTableModel dtm = (DefaultTableModel) tabCustomers.getModel();
        dtm.setRowCount(0);
        for (UserAccount userAccount : this.ecosystem.getUserAccountDirectory().getUserAccountList()) {
            if(userAccount.getRole() instanceof CustomerRole)
            {
                Object row[] = new Object[2];
                row[0] = userAccount.getUsername();
                row[1] = userAccount.getPassword();
                dtm.addRow(row);
            }
            
        }
    }
